//Adding//
function addNum(p1,p2){
    return p1 + p2;
}
//Subtraction//
function subNum(p1,p2){
    return p1 - p2;
}
//Multiply//
function mulNum(p1,p2){
    return p1 * p2;
}
//Remainder//
function remNum(p1,p2){
    return p1 % p2;
}
//Add More//
function moreNum(p1){
    return ++p1;
}
//Less//
function lessNum(p1){
    return --p1;
}

